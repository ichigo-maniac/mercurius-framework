CREATE TABLE SHOP_CATALOGS(
  uuid CHARACTER VARYING(36) NOT NULL PRIMARY KEY,
  name CHARACTER VARYING(255) NOT NULL,
  code CHARACTER VARYING(255) NOT NULL UNIQUE,
  creationtime TIMESTAMP NOT NULL,
  modificationtime TIMESTAMP NOT NULL
);

CREATE UNIQUE INDEX SHOP_CATALOG_UUID_IDX ON SHOP_CATALOGS(uuid);
CREATE UNIQUE INDEX SHOP_CATALOG_CODE_IDX ON SHOP_CATALOGS(code);
CREATE INDEX SHOP_CATALOG_NAME_IDX ON SHOP_CATALOGS(name);

CREATE TABLE BIG_STRINGS (
  uuid CHARACTER VARYING(36) NOT NULL PRIMARY KEY,
  name CHARACTER VARYING(255) NOT NULL,
  code CHARACTER VARYING(255) NOT NULL UNIQUE,
  creationtime TIMESTAMP NOT NULL,
  modificationtime TIMESTAMP NOT NULL,
  catalog_uuid CHARACTER VARYING(36) NOT NULL REFERENCES SHOP_CATALOGS(uuid) ON DELETE RESTRICT,
  text_value TEXT NOT NULL
);

CREATE UNIQUE INDEX BIG_STRING_UUID_IDX ON BIG_STRINGS(uuid);
CREATE INDEX BIG_STRING_CODE_IDX ON BIG_STRINGS(code);
CREATE INDEX BIG_STRING_NAME_IDX ON BIG_STRINGS(name);

